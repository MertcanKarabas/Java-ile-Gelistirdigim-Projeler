    private void newGame() {
        
        pnl_oyunEkrani.removeAll();
        pnl_oyunEkrani.revalidate();
        pnl_oyunEkrani.repaint();
        String cmbxDegeri = cmbx_butonSayisi.getSelectedItem().toString();
        int butonSayisi = Integer.parseInt(cmbxDegeri);
        int bolme = pnl_oyunEkrani.getWidth() / butonSayisi;
        int harflendirme = 65;

        for (int i = 0; i < butonSayisi; i++) {
            for (int j = 0; j < butonSayisi; j++) {
                JButton buton = new JButton("x");
                if (i == 0 || j == 0) {
                    String panelDisiHarflendirme = "";
                    JLabel harf_label = new JLabel();

                    if (i == 0 && j == 0) {
                        panelDisiHarflendirme += j + 1;
                        harf_label.setText(panelDisiHarflendirme);
                        harf_label.setSize(20, 20);
                        pnl_arkaEkran.add(harf_label);
                        harf_label.setLocation((pnl_oyunEkrani.getX() + 20) + (bolme * j), pnl_oyunEkrani.getY() - 25);
                        panelDisiHarflendirme = "";
                        harf_label = new JLabel();
                        panelDisiHarflendirme += (char) (harflendirme + i);
                        harf_label.setText(panelDisiHarflendirme);
                        harf_label.setSize(20, 20);
                        pnl_arkaEkran.add(harf_label);
                        harf_label.setLocation(pnl_oyunEkrani.getX() - 20, pnl_oyunEkrani.getY() + (bolme * i) + 15);

                    } else if (i == 0) {
                        panelDisiHarflendirme += j + 1;
                        harf_label.setText(panelDisiHarflendirme);
                        harf_label.setSize(20, 20);
                        pnl_arkaEkran.add(harf_label);
                        harf_label.setLocation((pnl_oyunEkrani.getX() + 20) + (bolme * j), pnl_oyunEkrani.getY() - 25);
                    } else if (j == 0) {
                        panelDisiHarflendirme += (char) (harflendirme + i);
                        harf_label.setText(panelDisiHarflendirme);
                        harf_label.setSize(20, 20);
                        pnl_arkaEkran.add(harf_label);
                        harf_label.setLocation(pnl_oyunEkrani.getX() - 20, pnl_oyunEkrani.getY() + (bolme * i) + 15);
                    }
                }
                if (butonSayisi > 8) {
                    buton.setSize(40, 40);
                } else {
                    buton.setSize(50, 50);
                }

                if (butonSayisi % 2 == 1) {
                    if (butonSayisi / 2 == i && butonSayisi / 2 == j) {
                        pnl_oyunEkrani.add(buton);
                        buton.setLocation(bolme * i, bolme * j);
                        buton.setEnabled(false);
                        buton.setText("");
                    }
                    pnl_oyunEkrani.add(buton);
                    buton.setLocation(bolme * i, bolme * j);
                } else {
                    if ((butonSayisi / 2 == i && butonSayisi / 2 == j) || ((butonSayisi / 2) == i && (butonSayisi / 2) - 1 == j)
                            || ((butonSayisi / 2) - 1 == i && butonSayisi / 2 == j) || ((butonSayisi / 2) - 1 == i && (butonSayisi / 2) - 1 == j)) {
                        pnl_oyunEkrani.add(buton);
                        buton.setLocation(bolme * i, bolme * j);
                        buton.setEnabled(false);
                        buton.setText("");
                    }
                    pnl_oyunEkrani.add(buton);
                    buton.setLocation(bolme * i, bolme * j);
                }
            }

        }

        LinkedList<String> list = new LinkedList();
        list.createList(butonSayisi);
        list.deleteWhenGameStart(butonSayisi);
    }

